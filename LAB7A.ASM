; PROGRAM  :: READ YOUR NAME FROM THE KEYBOARD AND DISPLAY IT AT A
; SPECIFIED LOCATION ON THE SCREEN IN FRONT OF THE MESSAGE 'WHAT IS YOUR
; NAME ?' YOU MUST CLEAR THE ENTIRE SCREEN BEFORE DISPLAY

.MODEL SMALL

DISPLAY MACRO MSG
        LEA DX, MSG
        MOV AH, 09H
        INT 21H
ENDM

.DATA
MSG DB 0DH, 0AH, "ENTER YOUR NAME : $"
MSG1 DB "What is your name ? $"
STRING DB 30H DUP(?)
ROW EQU  7D            
COLUMN EQU  20D

.CODE
START : MOV AX, @DATA
        MOV DS, AX
        DISPLAY MSG
        MOV SI, OFFSET STRING
        XOR CL, CL
AGAIN:  MOV AH, 01H
        INT 21H
        CMP AL, 0DH
        JE NEXT
        MOV [SI], AL
        INC SI
        INC CL
        JMP AGAIN
NEXT :  MOV [SI], BYTE PTR '$'
; CLEAR THE ENTIRE SCREEN
        MOV AL, 03H
        MOV AH, 00H
        INT 10H
; MOVE THE CURSOR TO THE SPECIFIED POSITION
        MOV BH, 00H
        MOV DH, ROW
        MOV DL, COLUMN
        MOV AH, 02H
        INT 10H
; DISPLAY THE MESSAGE..... AND NAME
        DISPLAY MSG1
        DISPLAY STRING
        MOV AH, 01H
        INT 21H
        MOV AH, 4CH
        INT 21H
END START
